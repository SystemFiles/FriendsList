    /**
     *
     * This method will, using the Bukkit API, send a private message to a
     * friend that is currently present on the senders friends list
     *
     * @param sender same as callers super-class
     * @param cmd same as callers super-class
     * @param label same as callers super-class
     * @param args same as callers super-class
     * @throws UnsupportedEncodingException UTF-8 Encoding is not used.
     */
    public void messageFriend(CommandSender sender, Command cmd, String label, String[] args) throws UnsupportedEncodingException {

        Player p = (Player) sender;
        final String MSG_FROM = ChatColor.YELLOW + " (" + ChatColor.WHITE + p.getName() + ChatColor.YELLOW + ") : ";

        if (args.length < 3) {
            p.sendMessage(MSG_PREFIX + "Proper usage is /friend msg <PlayerName> <Message>");
            Logger.getLogger(MSG_PREFIX + "Improper command usage /friend msg by " + p.getName());
        } else {
            if ((this.getConfig().contains(p.getName())) || (this.getConfig().getStringList(p.getName()) != null)) {
                List<String> friendsList = this.getConfig().getStringList(p.getName());
                if (friendsList.contains(args[1])) {
                    String fullMessage = StringUtils.join(args, ' ', 2, args.length);
                    @SuppressWarnings("LocalVariableHidesMemberVariable")
                    Player friend = Bukkit.getPlayer(args[1]);
                    if (Bukkit.getPlayer(args[1]) != null) { // if player is online.
                        if (!(friend.getName().equalsIgnoreCase(p.getName()))) {
                            friend.sendMessage(MSG_PREFIX + MSG_FROM + fullMessage);
                            p.sendMessage(MSG_PREFIX + ChatColor.GREEN + "Message sent to " + ChatColor.BOLD + args[1]);
                        } else {
                            p.sendMessage(MSG_PREFIX + "You can't message yourself, silly!");
                        }
                    } else {
                        p.sendMessage(MSG_PREFIX + "Sorry, " + args[1] + " is not online.");
                    }
                } else {
                    p.sendMessage(MSG_PREFIX + "Sorry, it seems that the person you are trying to message is not a friend.");
                    Logger.getLogger(MSG_PREFIX + "No friends list was found for " + p.getName());
                }
            } else {
                p.sendMessage(MSG_PREFIX + "Sorry, " + args[1] + "was not found in your friends list...");
            }
        }

    }

