    /**
     *
     * This method is used for the end-user to accept an incoming friend
     * request.
     *
     * @param sender Person executing the command
     * @param cmd The command being executed
     * @param label Any Alias associated
     * @param args Extra arguments to be passed into the command
     */
    public void acceptRequest(CommandSender sender, Command cmd, String label, String[] args) {

        Player p = (Player) sender;

        if (args.length < 2) {
            p.sendMessage(MSG_PREFIX + "Incorrect command usage. Proper usage: /friend accept <friendToAccept>");
        } else {
            if (requests.containsKey(p.getName())) {
                List<String> friendsList = this.getConfig().getStringList(p.getName());
                List<String> targetList = this.getConfig().getStringList(requests.get(p.getName()));
                targetList.add(p.getName()); // Adds Sender to targets friends list.
                this.getConfig().set(requests.get(p.getName()), targetList);
                this.saveConfig(); // saves the config.yml file.
                friendsList.add(args[1]); // Adds target to senders friends list.
                this.getConfig().set(p.getName(), friendsList);
                p.sendMessage(MSG_PREFIX + ChatColor.translateAlternateColorCodes('&', this.getConfig().getString("Messages.Added")).replace("$friend", args[1]));
                if (Bukkit.getPlayer(args[1]) != null) {
                    Player f = Bukkit.getPlayer(args[1]);
                    f.sendMessage(MSG_PREFIX + p.getName() + " has been added.");
                }
                requests.remove(p.getName());
            } else {
                p.sendMessage(MSG_PREFIX + "There are no requests for you.");
            }
        }
    }

