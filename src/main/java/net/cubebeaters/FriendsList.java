    /**
     *
     * Sends a gift to the specified user if he/she is a friend listed in this
     * plugin.
     *
     * @param sender Entity calling the command
     * @param cmd Command being called
     * @param label Alais (if Any)
     * @param args arguments to be passed by the method.
     * @throws UnsupportedEncodingException if UTF-8 Encoding is not used.
     */
    public void sendGift(CommandSender sender, Command cmd, String label, String[] args) throws UnsupportedEncodingException {

        Player p = (Player) sender;
        allowGift = this.getConfig().getBoolean("Plugin-Info.Allow-Gifting");

        if (allowGift) {
            if (args.length < 2) {
                p.sendMessage(MSG_PREFIX + "Incorrect Command usage. /fgift <PlayerName> <GiftMessage> is the correct usage.");
                Logger.getLogger(MSG_PREFIX + "Incorrect command usage by player, " + p.getName());
            } else {
                if (this.getConfig().getStringList(p.getName()).contains(args[1])) {
                    ItemStack senderItem = p.getInventory().getItemInHand();
                    if (Bukkit.getPlayer(args[1]) == null) {
                        p.sendMessage(MSG_PREFIX + "Sorry, " + args[1] + " is not currently online.");
                    } else {
                        @SuppressWarnings("LocalVariableHidesMemberVariable")
                        Player friend = Bukkit.getPlayer(args[1]);
                        PlayerInventory friendInventory = friend.getInventory();
                        if (senderItem.getType().equals(Material.AIR)) {
                            p.sendMessage(MSG_PREFIX + "You must add an item to your hand, in order to send it to your friend.");
                        } else {
                            friendInventory.addItem(senderItem);
                            p.setItemInHand(new ItemStack(Material.AIR)); // Remove Item from players inventory
                            p.sendMessage(MSG_PREFIX + "Your item has been sent!");
                            friend.sendMessage(MSG_PREFIX + "You have recieved a gift from " + p.getName() + ". You can thank him later :)");
                            if (args.length >= 3) {
                                String giftMessage = Arrays.toString(args).replaceAll("\\[", "").replaceAll("\\]", "").replace(args[1], "").replace(args[0], "");
                                friend.sendMessage(MSG_PREFIX + ChatColor.BOLD + "Gift Message: " + ChatColor.AQUA + giftMessage);
                            }
                        }
                    }
                } else {
                    p.sendMessage(MSG_PREFIX + "It appears that " + args[1] + " is not your friend.");
                }
            }

        } else {
            p.sendMessage(MSG_PREFIX + "Sorry, this option is not enabled in the config.yml");
            Logger.getLogger(MSG_PREFIX + "Player, " + p.getName() + " attempted to use /fgift when it is not enabled.");
        }

    }

